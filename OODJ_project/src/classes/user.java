package classes;


public abstract class user {
    
    // The methods below is called abstract method which is a "must" to be implemented in those classes that inherit this abstract class
    // Abstract method 1
    public abstract String verifyLogin();
    
    // Abstract method 2
    public abstract String managePersonalProfile();
    
    // Abstract method 3
    public abstract String getUserId();
    
    // Abstract method 4
    public abstract String getName();
    
    // Abstract method 5
    public abstract String getPassword();
    
    // Abstract method 6
    public abstract String getEmail();
    
    // Abstract method 7
    public abstract String getContact();
    
    // Abstract method 8
    public abstract String getGender();
    
    // Abstract method 9
    public abstract String getStatus();
}
